## Python allows users to key in inputs using the input keyword

## A simple console user log in form
first_name = input("Enter a valid firstname: ")
last_name = input("Enter a valid lastname: ")
password = input ("Enter a valid password: ")
email = input("Enter a valid email: ")

print(f"Hello {first_name} {last_name}, your password is {password}, and your email is {email}")

