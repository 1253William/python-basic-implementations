x = 10
y = 3
##Arithmetic Operators in python
print(x + y) # Output: 13
print(x - y) # Output: 7
print(x * y) # Output: 30
print(x / y) # Output: 3.333...
print(x % y) # Output: 1
print(x ** y) # Output: 1000
print(x // y) # Output: 3 //floor division

##Comparison Operators
a = 5
b = 8
print(a == b) # Output: False
print(a != b) # Output: True
print(a > b)  # Output: False
print(a < b)  # Output: True
print(a >= 5) # Output: True
print(b <= 8) # Output: True

##Logical Operators
p = True
q = False
print(p and q) # Output: False
print(p or q)  # Output: True
print(not p)  # Output: False

##Assigment Operators
num = 10
num += 5 # num = num + 5
print(num) # Output: 15